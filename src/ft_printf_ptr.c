#include "ft_printf.h"

int	ft_ptr_len(uintptr_t num)
{
	int	len;

	len = 0;
	while (num != 0)
	{
		len++;
		num = num / 16;
	}
	return (len);
}

void	ft_put_ptr(uintptr_t num)
{
	if (num >= 16)
	{
		ft_put_ptr(num / 16);
		ft_put_ptr(num % 16);
	}
	else
	{
		if (num <= 9)
			ft_putchar_fd((num + '0'), 1);
		else
			ft_putchar_fd((num - 10 + 'a'), 1);
	}
}

int	ft_print_ptr(unsigned long long ptr)
{
	int	p_len;

	p_len = 0;
	if (ptr == 0)
	{
		ft_putstr("(nil)");
		return (5);
	}
	else
	{
		p_len += write(1, "0x", 2);
		ft_put_ptr(ptr);
		p_len += ft_ptr_len(ptr);
	}
	return (p_len);
}
